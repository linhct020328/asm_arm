	AREA RESET, DATA, READONLY
		DCD 0X20001000
		DCD Reset_Handler

CHUOI DCD 2,3,4,5,7,9

	AREA MYCODE, CODE, READONLY

Reset_Handler
	MOV R0, #0; LUU TRUNG BINH CONG
	MOV R1, #0; LUU TONG
	LDR R2, = CHUOI; TRO DEN DIA CHI CHUOI
	MOV R3, #6; CHUA 7 PHAN TU TRONG CHUOI
	MOV R4, #0; BIEN DEM
LOOP
   	LDR R5, [R2], #4;
	ADD R1, R5
   	ADD R4, #1; TANG BIEN DEM
	CMP R3, R4
	BGT LOOP
	B TBCONG		

TBCONG
	SDIV R0, R1, R3; R0=R1/R3
	B STOP

STOP
	B STOP
	END