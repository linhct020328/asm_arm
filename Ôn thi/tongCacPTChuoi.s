	AREA RESET, DATA, READONLY
		DCD 0X20001000
		DCD Reset_Handler

CHUOI DCD 1,2,3,4,5,6
		 
	AREA MYCODE, CODE, READONLY

Reset_Handler
	MOV R3,#6;co 6 phan tu
	MOV R0,#0; LUU TONG CHUOI
	MOV R2,#0; BIEN DEM I
	LDR R1, =CHUOI; TRO DEN DIA CHI CHUOI
	
LOOP
	LDR R4, [R1],#4;
	ADD R0,R4;

	ADD R2,#1
	CMP R3,R2
	BGT LOOP
STOP
	B STOP
	END
