	AREA RESET, DATA, READONLY
		DCD 0X20001000
		DCD Reset_Handler

MATRAN 	DCD 1, 3, 4
		DCD	-4, 7, 4
		DCD 5, -5, 2

	AREA MYCODE, CODE, READONLY

Reset_Handler
   	MOV R0, #0; CHUA TONG
   	MOV R1, #9; MA TRAN 3X3 CO 9 PHANTU
	LDR R2, =MATRAN	; LAY DIA CHI MA TRAN
	MOV R3, #0; BIEN DEM

LOOP
	LDR R4, [R2], #4 ; LAY TUNG GIA TRI R4 THEO MATRAN, MOI PTU CAC NHAU 4 BYTE
	ADD R0, R4
	ADD R3, #1
	CMP R3, R1
	BLT LOOP

STOP
	B STOP
	END