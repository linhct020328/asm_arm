	AREA RESET, DATA, READONLY
	DCD 0x20001000
	DCD Reset_Handler

X EQU 2;
N EQU 3

	AREA MYCODE, CODE, READONLY

Reset_Handler
	MOV R0,#0; tinh tong
	MOV R1,#1; TINH TUNG X^N
	LDR R2, = X; TRO DEN X
	LDR R3, =N; TRO DEN N
	MOV R4,#0; BIEN CHAY I

LOOP
	 CMP R4,R3
	 BGT STOP
	 ADD R0,R1;
	 MUL R1,R2; NHAN TICH VOI X
	 ADD R4,#1; TANG BIEN DIEM
	 B LOOP
STOP
	B STOP

	END